// Comparison Operators

// -> Relational Operators
// > Greater then
// < Less then
// >= Greater then or Equal to
// <= Less then or Equal to

console.log(10 > 10); // false
console.log(10 < 10); //false

console.log(10 >= 10); // true
console.log(10 <= 10); // true

// -> Equality Operators
// ===, strict -> (type + value)
// !=== strict non equality operators (type + value)

// == lose equality operator (value)
// != lose non equality operator (value)

console.log(20 === 20); // true (type + value)
console.log(20 === "20"); // false (type + value)

console.log(20 !== 20); //false

console.log(20 == 20); // true (value)
console.log(20 == "20"); // true (value)

console.log(20 != 20); // false (value)
console.log(20 != "20"); // false
